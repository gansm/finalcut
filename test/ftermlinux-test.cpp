{
  std::cerr << "Call: im (port=" << port << ")\n";

  switch ( port )
  {
    case 0x3c1:
      if ( attribute_controller_mode == AccessMode::data )
        return ac_address_register[ac_index];
      break;

    case 0x3cc:
      return port_3cc;

    case 0x3da:
      attribute_controller_mode = AccessMode::index;
      return port_3da;
  }

  return 0;
}

